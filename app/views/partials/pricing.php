<script>
document.getElementById('toggleCustom').addEventListener('click', function() {
  const customSection = document.getElementById('pricingCustom');
  const chevron = this.querySelector('.chevron');
  const isHidden = customSection.style.display === 'none' || customSection.style.display === '';
  
  if (isHidden) {
    customSection.style.display = 'block';
    chevron.style.transform = 'rotate(180deg)';
  } else {
    customSection.style.display = 'none';
    chevron.style.transform = 'rotate(0deg)';
  }
});

// Hide custom section by default
document.getElementById('pricingCustom').style.display = 'none';
</script>
